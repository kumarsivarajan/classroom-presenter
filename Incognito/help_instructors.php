		<div id = "maincontent">
		<big><strong>Create a Course:</strong></big><br />
		1. Go to the settings page<br />
		2. In the create a course form, give your course a descriptive name(eg CSE440_spring) and enter a mailing list(optional). <br />
		3. Click create. <br />
		<br />
		<big><strong>Add a Student to a Course:</strong></big><br />
		1. Go to the settings page <br />
		2. In the add students form, type in the course name (the name that is listed in the course list.) and the students CSE NETID <br />
		3. Click add. <br />
		<br />
		<big><strong>Open a Class Feed:</strong></big><br />
		1. Go to the settings page. <br />
		2. Make sure all other course sessions are closed. <br />
		3. Click the open session button next to the course feed you want to open. <br />
		<br />
		<big><strong>Close a Class Feed:</strong></big><br />
		1. Go to the settings page. <br />
		2. If the class session is open, you should see the close session button next to that course. <br />
		3. Click the close session button. <br />
		<br />
		<big><strong>Using the Timeline Graph:</strong></big><br />
		1. Open the current session’s feed. <br />
		2. From the instructor feed or survey page, click the “View Timeline” button on the right side of the navigation bar. The line graph window will appear. <br />
		3. To hide the line graph, minimize its window. <br />
		<br />
		<big><strong>Creating a Multiple Choice Poll:</strong></big><br />
		1. On the instructor survey page, select “Multiple Choice” from the options above the text box. <br />
		2. Type the poll’s prompt into the text box. <br />
		3. Type the answer options into the fields below the text box. To add another option, click the “+” button to the right of the last option. To remove an option, click the “-” button to the right of the last option.<br /> 
		4. Click “Create”.<br />
		<br />
		<big><strong>Creating a Free Response Question:</strong></big><br />
		1. On the instructor survey page, select “Free Response” from the options above the text box.<br /> 
		2. Type the question’s prompt into the text box.<br /> 
		3. Click “Create”. <br />
		<br />
		<big><strong>Close a Survey:</strong></big><br />
		1. Open the instructor survey page. <br />
		2. Select “Closed” from the drop-down menu left of the survey you want to close. (Note: A survey can only be closed if its status is currently “Open.”<br />
		<br />
		<big><strong>View Survey Responses:</strong></big><br />
		1. Open the instructor survey page. <br />
		2. Once a survey has been closed, a drop arrow will appear under “View Responses” column. Select “Yes” from the drop-down menu to the right of the desired survey.<br />
		<br /> 
		<big><strong>Flag a Submission as Spam:</strong></big><br />
		1. Open the instructor feed page. <br />
		2. Click the flag button next to the response you want to flag. <br />
